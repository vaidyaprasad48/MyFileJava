package inheritance;

public class Mother //super class//type 1 single level inheritance
{
	public void nature()
	{
		System.out.println("Mother nature");
	}
	
	public void love()
	{
		System.out.println("Mother love");
	}
	
	
	///for static
	public static void look()
	{
		System.out.println("Mother look");
	}
	
	

}
