package loops;

public class ForLoop {

	public static void main(String[] args) 
	{
		// i want to print number 0-10 and increment by 1
		//for loop(initialization;condition;Updation
		for(int a=0; a<=10; a++)
		{
			System.out.println(a);
		}
		System.out.println("*************************");
		//	i want to print 2 table i.e 2-20
		
		for(int a=2; a<=20; a=a+2)
			System.out.println(a);
		System.out.println("******************************");
		
		//i want to print 5 table 5-50
		for(int a=5; a<=50; a=a+5)
			System.out.println(a);
		System.out.println("*****************************");
		
		//i want print number 10-0 and start with 10 decrement by 1
		for(int a=10; a>=0; a--)
			System.out.println(a);
		System.out.println("*************");
		//i want to print number 20-2 start with 2 decrement by 2
		for(int a=20; a>=2; a=a-2)
			System.out.println(a);
		System.out.println("****************");
		//i want to print 6 table 6-60
		for(int a=6; a<=60; a=a+6)
			System.out.println(a);
		System.out.println("***************");
		
		//i want to print 1-7 times
		for(int a=1;a<=7;a++)
			System.out.println(a);
		System.out.println("***************************");
		//i want to print velocity 8 times
		for(int a=1; a<=8; a++)
		System.out.println("velocity");
		
		System.out.println("*********************************");
		
		//i wnat to print Maharashi vaidya mandir 3times
		for(int a=1;a<=3; a++)
			System.out.println("maharashi vaidya mandir");
		
		System.out.println("****************************");
		
		//i want to print table of 3 i.e 3-30
		for(int a=3; a<=30; a=a+3)
		System.out.println(a);
		

	}

}
